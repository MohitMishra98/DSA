package Operators;
public class AOperator {
    public static void main(String args[]){
        // Binary (requires 2 operands)

        int a = 10;
        int b = 5;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        // Unary operators 
    }
    
}
