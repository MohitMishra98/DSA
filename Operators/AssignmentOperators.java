package Operators;
public class AssignmentOperators {
    public static void main(String args[]){
        // AssignmentOperators

        // a=b
        // a += b   a = a + b;
        // a -= b   a = a - b;
        // a *= b   a = a * b;
        // a /= b   a = a / b;
        // a %= b   a = a % b;

        int a = 10;
        int b = 5;

        a += b;
        System.out.println(a);

        a -= b;
        System.out.println(a);

        a *= b;
        System.out.println(a);

        a /= b;
        System.out.println(a);

        a %= b;
        System.out.println(a);
        
    }
    
}
