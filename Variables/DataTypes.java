package Variables;
public class DataTypes {
    public static void main(String args[]){

        //These are primitive datatypes

        byte b = 8;
        System.out.println(b);
        // byte has a range of 256 numbers
        // size of 1 byte and range of [-128 to 127]

        char ch = 'a';
        System.out.println(ch);
        // char can store a single character
        // size of 2 byte

        boolean var = true;
        System.out.println(var);
        // boolean is used to store true and false
        // size of 1 byte

        float price = 10.56f;
        System.out.println(price);
        // used to store int with . values
        // size of 4 byte

        int number = 25;
        System.out.println(number);
        // used to store int
        // size of 4 byte 

        // long is used to store int datatype with larger values 
        // size of 8 byte

        // double is used to store float datatype with larger values
        // size of 8 byte

        short n = 240;
        System.out.println(n);
        // size of 2 byte
    }
    
}
