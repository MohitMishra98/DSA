package Variables;
public class TypeCasting {
    public static void main(String args[]){
        // Type Casting
        // changing from 1 datatype to another

        float a = 25.12f;
        int b = (int)a;
        // float to int type casting

        System.out.println(b);

        // this is type conversion
        char ch = 'a';
        int number = ch;
        System.out.println(number);


    }
    
}
